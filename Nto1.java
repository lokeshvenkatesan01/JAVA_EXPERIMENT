import java.util.*;

public class Nto1 {
    public static void main(String[] args) {       
    
         Scanner sc=new Scanner(System.in); 
         System.out.print("ENTER THE N VALUE : ");
         int n=sc.nextInt();
         while(n>=1)
       {
        System.out.println(n);
        n--;
       }
    }
}
